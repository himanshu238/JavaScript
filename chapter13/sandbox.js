// Enter Switch statement

const grade = 'a';

switch (grade) {
    
    case 'A':
        console.log('Grade A!!');
        break;
    case 'B':
        console.log('Grade B');
        break;
    case 'C':
        console.log('Grade C');
        break;
    case 'D':
        console.log('Grade D');
        break;
    default:
        console.log('pass!!')
}